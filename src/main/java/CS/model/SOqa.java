package CS.model;

public class SOqa {
    String q = "";
    String a = "";
    long qScore = 0;
    long aScore = 0;

    public String getQ() {
        return q;
    }

    public void setQ(String q) {
        this.q = q;
    }

    public String getA() {
        return a;
    }

    public void setA(String a) {
        this.a = a;
    }

    public long getqScore() {
        return qScore;
    }

    public void setqScore(long qScore) {
        this.qScore = qScore;
    }

    public long getaScore() {
        return aScore;
    }

    public void setaScore(long aScore) {
        this.aScore = aScore;
    }
}
